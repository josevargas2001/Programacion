print("********** Multiplicacion **********")

multiplicacion=lambda num1,num2:num1*num2
resultado=multiplicacion(10, 4)
print(resultado)

print("********** Suma  ************* ")

suma=lambda num1,num2:num1+num2
resultado=suma(10, 4)
print(resultado)

print("********** Resta ************* ")

resta=lambda num1,num2:num1-num2
resultadoresta=resta(10, 4)
print(resultadoresta)

print("******* Divicion *********")
division=lambda num1,num2:num1/num2
resultadodivision=division(10, 4)
print(resultadodivision)
